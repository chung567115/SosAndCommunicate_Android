package com.chung.javabean;

public class Comments {
	private String commentPic;
	private int commentID;
	private String studentID;
	private String communicateBlogID;
	private String commentFloor;
	private String commentDetails;
	private String commentTime;
	private String commentLike;
	
	
	public String getCommentPic() {
		return commentPic;
	}
	public void setCommentPic(String commentPic) {
		this.commentPic = commentPic;
	}
	public int getCommentID() {
		return commentID;
	}
	public void setCommentID(int commentID) {
		this.commentID = commentID;
	}
	public String getStudentID() {
		return studentID;
	}
	public void setStudentID(String studentID) {
		this.studentID = studentID;
	}
	public String getCommunicateBlogID() {
		return communicateBlogID;
	}
	public void setCommunicateBlogID(String communicateBlogID) {
		this.communicateBlogID = communicateBlogID;
	}
	public String getCommentFloor() {
		return commentFloor;
	}
	public void setCommentFloor(String commentFloor) {
		this.commentFloor = commentFloor;
	}
	public String getCommentDetails() {
		return commentDetails;
	}
	public void setCommentDetails(String commentDetails) {
		this.commentDetails = commentDetails;
	}
	public String getCommentTime() {
		return commentTime;
	}
	public void setCommentTime(String commentTime) {
		this.commentTime = commentTime;
	}
	public String getCommentLike() {
		return commentLike;
	}
	public void setCommentLike(String commentLike) {
		this.commentLike = commentLike;
	}
	
	
	
}
