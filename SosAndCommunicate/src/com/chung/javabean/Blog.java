package com.chung.javabean;

public class Blog {
	private String goalsRank;
	private String headPic;
	private String name;
	private String studentID;
	private String theme;
	private String question;
	private String addr;
	private String date;
	private String phone;
	private String blogDetails;
	private String blogTime;
	private String blogType;
	private int blogID;
	
	
	public String getGoalsRank() {
		return goalsRank;
	}
	public void setGoalsRank(String goalsRank) {
		this.goalsRank = goalsRank;
	}
	public String getHeadPic() {
		return headPic;
	}
	public void setHeadPic(String headPic) {
		this.headPic = headPic;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStudentID() {
		return studentID;
	}
	public void setStudentID(String studentID) {
		this.studentID = studentID;
	}
	public String getTheme() {
		return theme;
	}
	public void setTheme(String theme) {
		this.theme = theme;
	}
	public String getQuestion() {
		return question;
	}
	public void setQuestion(String question) {
		this.question = question;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getBlogDetails() {
		return blogDetails;
	}
	public void setBlogDetails(String blogDetails) {
		this.blogDetails = blogDetails;
	}
	public String getBlogTime() {
		return blogTime;
	}
	public void setBlogTime(String blogTime) {
		this.blogTime = blogTime;
	}
	public String getBlogType() {
		return blogType;
	}
	public void setBlogType(String blogType) {
		this.blogType = blogType;
	}
	public int getBlogID() {
		return blogID;
	}
	public void setBlogID(int blogID) {
		this.blogID = blogID;
	}

}
